#include "libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	if (n == 0)
		return (1);
	if (s1 == NULL || s2 == NULL)
		return (0);
	if (ft_strlen(s1) != ft_strlen(s2))
		return (0);
	else
		while (i < n && s1[i] != '\0')
		{
			if (s1[i] != s2[i])
				return (0);
			i++;
		}
	return (1);
}
